      <h2> What is ATA system ? </h2><br>
      <p>ATS (Amanah Tugas Syarahan) also known as ATA ( Amanah Tugas Akademik)
        is a system that keep track workload of lecturers for every faculty.
        It involves the calculation of teaching hour, number of students
        to be taught, number of subjects and assigning the courses and group
        of students to the lecturers. Subjects given are in accordance to the
        lecturers' qualification and experiences. In this system, we ensure
        that the flow of the system is guaranteedly smooth and will help to
        reduce the burden of the lecturers. We have discussed and investigated
        the processes that are going to be run in this system with the head
        coordinator who started this system. So, with the works we believed
        that this system will do its work properly without any interuptions.</p>
